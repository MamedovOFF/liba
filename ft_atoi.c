#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	pos;
	int	neg;

	i = 0;
	pos = 0;
	neg = 1;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		pos = pos * 10 + str[i] - '0';
		i++;
	}
	return (pos * neg);
}
